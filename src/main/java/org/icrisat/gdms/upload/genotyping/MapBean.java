package org.icrisat.gdms.upload.genotyping;

public class MapBean {
	private int map_id;
	private String map_name;
	private String map_type;
	private int mp_id;
	private String map_desc;
	private String map_unit;
	private String genus;
	private String species;
	private String institute;
	
	
	
	
	public String getGenus() {
		return genus;
	}
	public void setGenus(String genus) {
		this.genus = genus;
	}
	public String getSpecies() {
		return species;
	}
	public void setSpecies(String species) {
		this.species = species;
	}
	public String getInstitute() {
		return institute;
	}
	public void setInstitute(String institute) {
		this.institute = institute;
	}
	public int getMap_id() {
		return map_id;
	}
	public void setMap_id(int map_id) {
		this.map_id = map_id;
	}
	public String getMap_name() {
		return map_name;
	}
	public void setMap_name(String map_name) {
		this.map_name = map_name;
	}
	public String getMap_type() {
		return map_type;
	}
	public void setMap_type(String map_type) {
		this.map_type = map_type;
	}
	public int getMp_id() {
		return mp_id;
	}
	public void setMp_id(int mp_id) {
		this.mp_id = mp_id;
	}
	public String getMap_desc() {
		return map_desc;
	}
	public void setMap_desc(String map_desc) {
		this.map_desc = map_desc;
	}
	public String getMap_unit() {
		return map_unit;
	}
	public void setMap_unit(String map_unit) {
		this.map_unit = map_unit;
	}
	
	

}
